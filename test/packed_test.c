static void assert_traverse_result(const unsigned char* data, size_t len,
                                   packed_error_t expected_err,
                                   const unsigned char* expected_res_data,
                                   size_t expected_res_data_len) {
  /* Load data */
  struct cbor_load_result res;
  cbor_item_t* item = cbor_load(data, len, &res);
  assert(res.error.code == CBOR_ERR_NONE);
  cbor_item_t* expected_res =
      cbor_load(expected_res_data, expected_res_data_len, &res);
  assert(res.error.code == CBOR_ERR_NONE);

  /* Performe _traverse operation */
  cbor_item_t* resp = cbor_unpack(item, NULL);
  if (expected_err != PACKED_ERR_NONE) {
    assert(resp == NULL);
  } else {
    assert(resp != NULL);
    assert(cbor_structurally_equal(resp, expected_res));
  }

  // cbor_decref(&expected_res);
}

